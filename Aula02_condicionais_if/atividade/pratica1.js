numero = prompt('Digitte um numero de 1 a 5: ');

if(numero == '1'){
    alert('numero um.');
}
else if(numero == '2'){
    alert('numero dois.');
}
else if(numero == '3'){
    alert('numero tres.');
}
else if(numero == '4'){
    alert('numero quatro.');
}
else if(numero == '5'){
    alert('numero cinco.');
}
else{
    alert('numero errado!');
}
